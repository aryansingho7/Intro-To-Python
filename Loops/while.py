## While Loops
# SYNTAX - while expression:
#             statement(s)

count = 0
while(count < 3):
    count = count + 1
    print("Heelo World")

a = [1,2,3,4]
while a:
    print(a.pop())

# Single statement while block

count = 0
while (count < 5): count = count + 1; print("Hello Geeks")











